// 1475. 방번호

#include <bits/stdc++.h>

using namespace std;

int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);

    int a, x, num[9] = {};

    cin >> a;

    while(a>0)
    {
        x = a%10;
        if(x == 6 || x == 9) num[6]++;
        else num[x]++;
        a = a/10;
    }
    num[6] = num[6]/2 + num[6]%2;
    
    cout << *max_element(num, num+9) << "\n";
}

